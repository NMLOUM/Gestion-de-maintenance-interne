<script setup>
import { computed } from 'vue'

const props = defineProps({
    ticket: {
        type: Object,
        required: true
    },
    showDetails: {
        type: Boolean,
        default: false
    }
})

const slaInfo = computed(() => props.ticket.sla_status || {})

const badgeClasses = computed(() => {
    const baseClasses = 'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium'

    const colorClasses = {
        'red': 'bg-red-100 text-red-800',
        'orange': 'bg-orange-100 text-orange-800',
        'green': 'bg-green-100 text-green-800',
        'gray': 'bg-gray-100 text-gray-800'
    }

    return `${baseClasses} ${colorClasses[slaInfo.value.color] || colorClasses.gray}`
})

const iconClasses = computed(() => {
    const baseClasses = 'mr-1 h-3 w-3'

    const colorClasses = {
        'red': 'text-red-600',
        'orange': 'text-orange-600',
        'green': 'text-green-600',
        'gray': 'text-gray-600'
    }

    return `${baseClasses} ${colorClasses[slaInfo.value.color] || colorClasses.gray}`
})
</script>

<template>
    <div class="inline-flex items-center gap-2">
        <span :class="badgeClasses">
            <!-- Icon basé sur le statut -->
            <svg v-if="slaInfo.status === 'overdue'" :class="iconClasses" fill="currentColor" viewBox="0 0 20 20">
                <path fill-rule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z" clip-rule="evenodd" />
            </svg>
            <svg v-else-if="slaInfo.status === 'critical'" :class="iconClasses" fill="currentColor" viewBox="0 0 20 20">
                <path fill-rule="evenodd" d="M8.257 3.099c.765-1.36 2.722-1.36 3.486 0l5.58 9.92c.75 1.334-.213 2.98-1.742 2.98H4.42c-1.53 0-2.493-1.646-1.743-2.98l5.58-9.92zM11 13a1 1 0 11-2 0 1 1 0 012 0zm-1-8a1 1 0 00-1 1v3a1 1 0 002 0V6a1 1 0 00-1-1z" clip-rule="evenodd" />
            </svg>
            <svg v-else-if="slaInfo.status === 'warning'" :class="iconClasses" fill="currentColor" viewBox="0 0 20 20">
                <path fill-rule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zm1-12a1 1 0 10-2 0v4a1 1 0 00.293.707l2.828 2.829a1 1 0 101.415-1.415L11 9.586V6z" clip-rule="evenodd" />
            </svg>
            <svg v-else-if="slaInfo.status === 'ok'" :class="iconClasses" fill="currentColor" viewBox="0 0 20 20">
                <path fill-rule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z" clip-rule="evenodd" />
            </svg>
            <svg v-else :class="iconClasses" fill="currentColor" viewBox="0 0 20 20">
                <path fill-rule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zm1-11a1 1 0 10-2 0v2H7a1 1 0 100 2h2v2a1 1 0 102 0v-2h2a1 1 0 100-2h-2V7z" clip-rule="evenodd" />
            </svg>

            <span>{{ slaInfo.label || 'N/A' }}</span>
        </span>

        <!-- Détails optionnels -->
        <span v-if="showDetails && slaInfo.formatted" class="text-xs text-gray-600">
            {{ slaInfo.formatted }}
        </span>
    </div>
</template>
